class TestDetectAISignals(unittest.TestCase):
    """Tests for detect_ai_signals()."""

    def test_copilot_coauthor(self):
        msg = "feat: add auth\n\nCo-authored-by: Copilot <copilot@github.com>"
        signals = cli.detect_ai_signals(msg)
        self.assertTrue(len(signals) > 0)
        self.assertTrue(any("copilot" in s for s in signals))

    def test_chatgpt_generated(self):
        msg = "refactor: update parser\n\nGenerated by ChatGPT"
        signals = cli.detect_ai_signals(msg)
        self.assertTrue(any("chatgpt" in s for s in signals))

    def test_claude_code(self):
        msg = "fix: resolve null check\n\nClaude Code assisted this change"
        signals = cli.detect_ai_signals(msg)
        self.assertTrue(any("claude code" in s for s in signals))

    def test_aider_prefix(self):
        msg = "aider: refactored the auth module"
        signals = cli.detect_ai_signals(msg)
        self.assertTrue(any("aider" in s for s in signals))

    def test_no_signals_human(self):
        msg = "fix: typo in README\n\nSimple fix."
        signals = cli.detect_ai_signals(msg)
        self.assertEqual(len(signals), 0)

    def test_bot_author_name(self):
        signals = cli.detect_ai_signals(
            "chore: update deps",
            author_name="dependabot[bot]",
            author_email="dependabot@github.com",
        )
        self.assertTrue(any("dependabot" in s for s in signals))

    def test_bot_committer(self):
        signals = cli.detect_ai_signals(
            "update lock",
            committer_name="renovate[bot]",
            committer_email="renovate@example.com",
        )
        self.assertTrue(any("renovate" in s for s in signals))

    def test_trailer_detected(self):
        msg = "feat: add feature\n\nGenerated-by: copilot-v2"
        signals = cli.detect_ai_signals(msg)
        # VULN-07: Should only record trailer key, not the value
        trailer_signals = [s for s in signals if s.startswith("trailer:")]
        for s in trailer_signals:
            self.assertNotIn("copilot-v2", s, "Trailer value should not be leaked")
            self.assertIn("generated-by", s)

    def test_trailer_value_not_leaked(self):
        """VULN-07: Internal tool names in trailers must not appear in signals."""
        msg = "fix: update\n\nTool: proprietary-internal-system-v3.2"
        signals = cli.detect_ai_signals(msg)
        trailer_signals = [s for s in signals if s.startswith("trailer:")]
        self.assertTrue(len(trailer_signals) > 0)
        for s in trailer_signals:
            self.assertNotIn("proprietary", s)

    def test_case_insensitive(self):
        msg = "chore: update\n\nCO-AUTHORED-BY: COPILOT"
        signals = cli.detect_ai_signals(msg)
        self.assertTrue(len(signals) > 0)

    def test_multiple_signals(self):
        msg = "AI-generated code\n\nCo-authored-by: Copilot\nGenerated-by: copilot"
        signals = cli.detect_ai_signals(msg)
        self.assertTrue(len(signals) >= 2)

    def test_windsurf_signal(self):
        msg = "feat: new component\n\nCreated with Windsurf editor"
        signals = cli.detect_ai_signals(msg)
        self.assertTrue(any("windsurf" in s for s in signals))


