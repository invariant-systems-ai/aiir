class TestAISignalCoverage(unittest.TestCase):
    """R21-PUB-01: AI_SIGNALS list must include all major tools."""

    def test_ai_signals_count(self):
        """AI_SIGNALS must have at least 30 patterns (was 20 before R21)."""
        self.assertGreaterEqual(len(cli.AI_SIGNALS), 30)

    def test_major_tools_in_signals(self):
        """All major AI coding tools must be in AI_SIGNALS."""
        required = [
            "amazon q",
            "codewhisperer",
            "co-authored-by: devin",
            "gemini code assist",
            "google gemini",
            "tabnine",
            "github copilot",
            "claude code",
            "windsurf",
            "aider:",
            "codeium",
            "sourcegraph cody",
        ]
        for tool in required:
            self.assertIn(tool, cli.AI_SIGNALS, f"Missing AI signal: {tool}")


