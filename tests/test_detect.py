class TestRedTeamSignalEvasion(unittest.TestCase):
    """Tests for R5-01/02/03 signal evasion fixes."""

    def test_r5_01_combining_mark_evasion_blocked(self):
        """Combining diacritical marks must not evade signal detection."""
        # U+0336 COMBINING LONG STROKE OVERLAY between 'o' and 'p'
        msg = "Co-authored-by: co\u0336pilot"
        signals, _ = cli.detect_ai_signals(msg)
        self.assertTrue(
            len(signals) > 0, f"Combining mark evasion not blocked: {msg!r}"
        )

    def test_r5_02_variation_selector_evasion_blocked(self):
        """Variation selectors must not evade signal detection."""
        # U+FE0E VARIATION SELECTOR-15 between 'p' and 'i'
        msg = "Co-authored-by: cop\ufe0eilot"
        signals, _ = cli.detect_ai_signals(msg)
        self.assertTrue(
            len(signals) > 0, f"Variation selector evasion not blocked: {msg!r}"
        )

    def test_r5_03_tab_whitespace_evasion_blocked(self):
        """Tab characters in trailers must not evade signal detection."""
        msg = "Co-authored-by:\tcopilot"
        signals, _ = cli.detect_ai_signals(msg)
        self.assertTrue(
            len(signals) > 0, f"Tab whitespace evasion not blocked: {msg!r}"
        )

    def test_r5_03_double_space_evasion_blocked(self):
        """Double spaces in trailers must not evade signal detection."""
        msg = "Co-authored-by:  copilot"
        signals, _ = cli.detect_ai_signals(msg)
        self.assertTrue(len(signals) > 0, f"Double-space evasion not blocked: {msg!r}")

    def test_r5_01_normal_signals_still_detected(self):
        """Normal signals must still be detected after Mn/Me stripping."""
        msg = "Co-authored-by: copilot"
        signals, _ = cli.detect_ai_signals(msg)
        self.assertTrue(len(signals) > 0)


