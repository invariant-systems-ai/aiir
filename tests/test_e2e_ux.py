    # ── 12. Receipt format validation ───────────────────────────────────

    def test_12_receipt_format(self):
        """Receipt JSON has every documented field with correct types."""
        rc, stdout, _ = self._run_cli("--json")
        self.assertEqual(rc, 0)
        receipt = json.loads(stdout)

        # Top-level fields
        self.assertEqual(receipt["type"], "aiir.commit_receipt")
        self.assertEqual(receipt["schema"], "aiir/commit_receipt.v2")
        self.assertIsInstance(receipt["version"], str)
        self.assertTrue(receipt["receipt_id"].startswith("g1-"))
        self.assertTrue(receipt["content_hash"].startswith("sha256:"))
        self.assertIsInstance(receipt["timestamp"], str)
        self.assertIn("T", receipt["timestamp"])  # ISO 8601

        # commit block
        commit = receipt["commit"]
        self.assertIsInstance(commit["sha"], str)
        self.assertEqual(len(commit["sha"]), 40)
        self.assertIsInstance(commit["author"], dict)
        self.assertIn("name", commit["author"])
        self.assertIn("email", commit["author"])
        self.assertIsInstance(commit["subject"], str)
        self.assertTrue(commit["diff_hash"].startswith("sha256:"))
        self.assertIsInstance(commit["files_changed"], int)

        # ai_attestation block
        ai = receipt["ai_attestation"]
        self.assertIsInstance(ai["is_ai_authored"], bool)
        self.assertIsInstance(ai["signals_detected"], list)
        self.assertIsInstance(ai["signal_count"], int)
        self.assertIsInstance(ai["is_bot_authored"], bool)
        self.assertIsInstance(ai["bot_signals_detected"], list)
        self.assertIsInstance(ai["bot_signal_count"], int)
        self.assertIn(
            ai["authorship_class"],
            [
                "human",
                "ai_assisted",
                "bot",
                "ai+bot",
            ],
        )
        self.assertEqual(ai["detection_method"], "heuristic_v2")

        # extensions block
        self.assertIn("extensions", receipt)
        self.assertIsInstance(receipt["extensions"], dict)

    # ── 13. Content-addressability ──────────────────────────────────────

    def test_13_content_addressability(self):
        """receipt_id = g1-<hash> of canonical JSON core."""
        rc, stdout, _ = self._run_cli("--json")
        self.assertEqual(rc, 0)
        receipt = json.loads(stdout)

        # Reconstruct core (everything except receipt_id, content_hash,
        # timestamp, extensions — exactly what build_commit_receipt hashes)
        core = {
            k: v
            for k, v in receipt.items()
            if k not in ("receipt_id", "content_hash", "timestamp", "extensions")
        }
        core_json = _canonical_json(core)
        expected_hash = _sha256(core_json)
        expected_id = f"g1-{expected_hash[:32]}"
        expected_content_hash = f"sha256:{expected_hash}"

        self.assertEqual(receipt["receipt_id"], expected_id)
        self.assertEqual(receipt["content_hash"], expected_content_hash)

