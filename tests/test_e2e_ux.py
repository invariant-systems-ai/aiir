"""
End-to-end dry-run test — guarantees the documented UX never breaks.

This module creates a realistic multi-commit git repo (human, AI, bot)
and exercises every CLI path documented in README.md:

  1.  aiir                              → ledger default (.aiir/)
  2.  aiir --pretty                     → ledger + human-readable stderr
  3.  aiir --range A..B --pretty        → range mode
  4.  aiir --ai-only                    → filter out non-AI
  5.  aiir --ai-only --output .rcpts/   → filtered + directory output
  6.  aiir --json                       → stdout JSON
  7.  aiir --jsonl                      → stdout JSONL
  8.  aiir --ledger .audit/             → custom ledger path
  9.  aiir --verify receipt.json        → round-trip integrity check
  10. Deduplication                     → re-run is no-op
  11. index.json structure              → all documented fields
  12. Receipt format                    → every field present + typed
  13. Content-addressability            → receipt_id = hash(canonical JSON)
  14. AI detection                      → Copilot / ChatGPT / Claude signals
  15. Bot detection                     → dependabot / github-actions
  16. authorship_class values           → human / ai_assisted / bot
  17. --redact-files                    → files omitted, files_redacted present
  18. --github-action                   → outputs set correctly
  19. --version                         → prints current version
  20. --help                            → includes usage examples
  21. --json piped (single commit)      → valid JSON object
  22. Ledger export (--export)          → portable JSON bundle
  23. Stats / badge / check (ledger)    → post-generation analytics

If any of these documented contracts break, this test fails immediately.

Copyright 2025-2026 Invariant Systems, Inc.
# SPDX-License-Identifier: Apache-2.0
"""

