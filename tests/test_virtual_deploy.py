class TestMCPServeStdio(unittest.TestCase):
    """Cover mcp_server.py lines 519-522, 571, 583-584, 632: serve_stdio paths."""

    def _run_mcp_session(self, requests):
        """Feed JSON-RPC requests through serve_stdio via mocked stdin/stdout."""
        from aiir.mcp_server import serve_stdio

        input_lines = "\n".join(json.dumps(r) for r in requests) + "\n"
        responses = []

        with patch("sys.stdin", io.StringIO(input_lines)):
            capture = io.StringIO()
            with patch("sys.stdout", capture):
                serve_stdio()

            capture.seek(0)
            for line in capture:
                line = line.strip()
                if line:
                    try:
                        responses.append(json.loads(line))
                    except json.JSONDecodeError:
                        pass
        return responses

    def test_notification_no_response(self):
        """Notifications (no id) should not produce responses."""
