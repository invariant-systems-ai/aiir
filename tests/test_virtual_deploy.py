_REPO_DIR: str = ""
_ORIG_CWD: str = ""


def setUpModule():
    global _REPO_DIR, _ORIG_CWD
    _ORIG_CWD = os.getcwd()
    _REPO_DIR = tempfile.mkdtemp(prefix="aiir_vdeploy_")
    _git(["init"])
    _git(["config", "user.name", "VDeploy Test"])
    _git(["config", "user.email", "vdeploy@test.local"])
    _git(["config", "commit.gpgSign", "false"])
    # Initial commit
    Path(_REPO_DIR, "README.md").write_text("# VDeploy\n")
    _git(["add", "."])
    _git(["commit", "-m", "initial commit"])
    # AI-authored commit
    Path(_REPO_DIR, "ai_file.py").write_text("# Generated by GitHub Copilot\n")
    _git(["add", "."])
    _git(["commit", "-m", "feat: add module\n\nGenerated by GitHub Copilot"])
    # Remote for provenance
    _git(["remote", "add", "origin", "https://github.com/test/repo.git"])


def tearDownModule():
    os.chdir(_ORIG_CWD)
    if _REPO_DIR:
        shutil.rmtree(_REPO_DIR, ignore_errors=True)


def _git(args):
    subprocess.run(
        ["git"] + args, cwd=_REPO_DIR,
        stdout=subprocess.PIPE, stderr=subprocess.PIPE, check=True,
    )


def _chdir_repo():
    """Helper: chdir into _REPO_DIR and return the previous directory."""
    prev = os.getcwd()
    os.chdir(_REPO_DIR)
    return prev


